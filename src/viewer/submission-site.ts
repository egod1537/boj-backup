function renderNotFoundPage(username: string, origin: string): string {
  return renderSubmissionsNotFoundReactPage(username, origin);
}

function respondHtml(response: ServerResponse, html: string): void {
  response.writeHead(200, { "content-type": "text/html; charset=utf-8" });
  response.end(html);
}

function formatValueWithUnit(value: number | null, unit: string): string {
  if (value === null) {
    return "-";
  }

  return `${formatNumber(value)}${unit}`;
}

function formatDateTime(value: string): string {
  const date = new Date(value);

  if (Number.isNaN(date.getTime())) {
    return value;
  }

  return new Intl.DateTimeFormat("ko-KR", {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(date);
}

function formatNumber(value: number): string {
  return new Intl.NumberFormat("ko-KR").format(value);
}

function escapeHtml(value: string): string {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function normalizeServerStartError(error: unknown, host: string, port: number): Error {
  if (error && typeof error === "object" && "code" in error) {
    const code = String((error as { code?: unknown }).code);

    if (code === "EADDRINUSE") {
      return new ConfigurationError(`Port ${port} is already in use.`);
    }

    if (code === "EACCES") {
      return new ConfigurationError(
        `Could not bind to ${host}:${port}. That port may be restricted by Windows. Try --port 3000 or --port 0.`,
      );
    }
  }

  return error instanceof Error ? error : new Error(String(error));
}
