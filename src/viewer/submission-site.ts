const ROW_INDEX = {
  submissionId: 0,
  problemId: 1,
  problemTitle: 2,
  result: 3,
  resultClass: 4,
  memoryKb: 5,
  timeMs: 6,
  language: 7,
  codeLength: 8,
  submittedAt: 9,
  submittedAtTimestamp: 10,
} as const;

export interface StartSubmissionsViewerOptions {
  inputPath: string;
  host: string;
  port: number;
}

export interface StartedSubmissionsViewer {
  server: Server;
  username: string;
  inputPath: string;
  statusUrl: string;
}

export interface RenderSubmissionsStatusOptions {
  localProfileOrigin?: string | null;
  problemNav?: ReactRenderSubmissionsStatusOptions["problemNav"];
}

export async function startSubmissionsViewerServer(
  options: StartSubmissionsViewerOptions,
): Promise<StartedSubmissionsViewer> {
  const inputPath = path.resolve(options.inputPath);
  const snapshot = await readSubmissionsSnapshot(inputPath);
  const username = snapshot.username;

  const server = createServer((request, response) => {
    const requestUrl = new URL(request.url ?? "/", `http://${request.headers.host ?? "localhost"}`);
    const pathname = decodeURIComponent(requestUrl.pathname);

    if (pathname === "/") {
      response.writeHead(302, {
        Location: `/status?user_id=${encodeURIComponent(username)}`,
      });
      response.end();
      return;
    }

    if (pathname === "/favicon.ico") {
      response.writeHead(302, { Location: BOJ_FAVICON_32_URL });
      response.end();
      return;
    }

    if (pathname === "/status") {
      const requestedUser = requestUrl.searchParams.get("user_id");

      if (!requestedUser || requestedUser === username) {
        respondHtml(response, renderSubmissionsStatusPage(snapshot, requestUrl.origin));
        return;
      }
    }

    response.writeHead(404, { "content-type": "text/html; charset=utf-8" });
    response.end(renderNotFoundPage(username, requestUrl.origin));
  });

  await new Promise<void>((resolve, reject) => {
    const onError = (error: unknown) => {
      reject(normalizeServerStartError(error, options.host, options.port));
    };

    server.once("error", onError);
    server.listen(options.port, options.host, () => {
      server.off("error", onError);
      resolve();
    });
  });

  const address = server.address();
  if (!address || typeof address === "string") {
    throw new Error("Could not determine submissions viewer server address.");
  }

  const displayHost = options.host === "0.0.0.0" ? "127.0.0.1" : options.host;
  const baseUrl = `http://${displayHost}:${address.port}`;

  return {
    server,
    username,
    inputPath,
    statusUrl: `${baseUrl}/status?user_id=${encodeURIComponent(username)}`,
  };
}

export function renderSubmissionsStatusPage(
  snapshot: BojUserSubmissionsSnapshot,
  origin: string,
  options: RenderSubmissionsStatusOptions = {},
): string {
  return renderSubmissionsStatusReactPage(snapshot, origin, options);
}

async function readSubmissionsSnapshot(inputPath: string): Promise<BojUserSubmissionsSnapshot> {
  let raw: string;

  try {
    raw = await readFile(inputPath, "utf8");
  } catch {
    throw new ConfigurationError(`Could not read submissions JSON: ${inputPath}`);
  }

  let parsed: unknown;

  try {
    parsed = JSON.parse(raw);
  } catch {
    throw new ConfigurationError(`Submissions JSON is not valid JSON: ${inputPath}`);
  }

  if (!isSubmissionsSnapshot(parsed)) {
    throw new ConfigurationError(
      "Submissions JSON must contain username, columns, and rows generated by the submissions command.",
    );
  }

  return parsed;
}

function isSubmissionsSnapshot(value: unknown): value is BojUserSubmissionsSnapshot {
  if (!value || typeof value !== "object") {
    return false;
  }

  const snapshot = value as Partial<BojUserSubmissionsSnapshot>;
  return (
    typeof snapshot.username === "string" &&
    Array.isArray(snapshot.columns) &&
    Array.isArray(snapshot.rows)
  );
}

function renderStatusTable(
  snapshot: BojUserSubmissionsSnapshot,
  options: RenderSubmissionsStatusOptions,
): string {
  const visibleColumns = snapshot.columns.filter((column) => column.visible);
  const headers = visibleColumns.map((column) => `<th>${escapeHtml(column.label)}</th>`).join("");
  const rows = snapshot.rows.map((row) => renderStatusRow(snapshot.username, row, options)).join("");

  return `<table class="table table-striped table-bordered" id="status-table"><thead><tr>${headers}</tr></thead><tbody>${rows}</tbody></table>`;
}

function renderStatusRow(
  username: string,
  row: BojUserSubmissionRow,
  options: RenderSubmissionsStatusOptions,
): string {
  const submissionId = row[ROW_INDEX.submissionId];
  const problemId = row[ROW_INDEX.problemId];
  const problemTitle = row[ROW_INDEX.problemTitle];
  const result = row[ROW_INDEX.result];
  const resultClass = row[ROW_INDEX.resultClass];
  const memoryKb = row[ROW_INDEX.memoryKb];
  const timeMs = row[ROW_INDEX.timeMs];
  const language = row[ROW_INDEX.language];
  const codeLength = row[ROW_INDEX.codeLength];
  const submittedAt = row[ROW_INDEX.submittedAt];

  const problemCell =
    problemId !== null
      ? `<a href="https://www.acmicpc.net/problem/${problemId}" rel="tooltip" data-placement="right" title="${escapeHtml(problemTitle ?? "")}" class="problem_title tooltip-click" target="_blank">${problemId}</a>`
      : "-";
  const resultCell = resultClass
    ? `<span class="result-text ${escapeHtml(resultClass)}">${escapeHtml(result)}</span>`
    : escapeHtml(result);
  const profileUrl = options.localProfileOrigin
    ? `${options.localProfileOrigin}/user/${encodeURIComponent(username)}`
    : `https://www.acmicpc.net/user/${encodeURIComponent(username)}`;
  const profileAttrs = options.localProfileOrigin ? "" : ' target="_blank" rel="noreferrer"';

  return `
    <tr id="solution-${submissionId}">
      <td class="viewer-submission-id">${submissionId}</td>
      <td><a href="${escapeHtml(profileUrl)}"${profileAttrs}><span class="user-blue">${escapeHtml(username)}</span></a></td>
      <td>${problemCell}</td>
      <td class="result">${resultCell}</td>
      <td class="memory">${escapeHtml(formatValueWithUnit(memoryKb, "KB"))}</td>
      <td class="time">${escapeHtml(formatValueWithUnit(timeMs, "ms"))}</td>
      <td>${escapeHtml(language)}</td>
      <td>${escapeHtml(formatValueWithUnit(codeLength, "B"))}</td>
      <td><a href="https://www.acmicpc.net/status?user_id=${encodeURIComponent(username)}" target="_blank" rel="noreferrer">${escapeHtml(submittedAt ?? "-")}</a></td>
    </tr>
  `;
}

